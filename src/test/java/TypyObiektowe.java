public class TypyObiektowe {

    // Typy obiektowe przechowują aktualną wartość oraz umożliwiają wykonanie akcji czyli wywołanie metod na tych danych.



    public static void main(String[] args) {
        // liczby stałoprzecinkowe

        Byte firstNumber = 127;
        Short secondNumber = 23689;
        Integer thirtNumber = 32768999;
        Long fourthNumber  = 2148000L;

        // liczby zmiennoprzecinkowe

        Float sixthNumber = 34.59F;
        Double fifthNumber = 4.09D;

        // wartość logiczna

        Boolean prawda = true;
        Boolean fałsz = false;

        //pojedynczy znak

        Character literka = 'A';

        String hello = "hello David";

        System.out.println(hello.charAt(4));


    }
}
